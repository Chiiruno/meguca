/// Get country name by ISO 3166 lowercase alpha-2 code
pub fn get_name(code: &str) -> Option<&'static str> {
	if code.len() != 2 {
		return None;
	}

	let arr = unsafe {
		[
			*code.as_bytes().get_unchecked(0),
			*code.as_bytes().get_unchecked(1),
		]
	};
	let mut left = 0;
	let mut right = COUNTRIES.len() - 1;
	while left <= right {
		let mid = (left + right) / 2;
		let key = COUNTRIES[mid].0;
		if arr < key {
			right = mid - 1;
		} else if arr > key {
			left = mid + 1;
		} else {
			return Some(COUNTRIES[mid].1);
		}
	}
	None
}
